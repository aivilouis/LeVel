package umn.ac.id.level;

public class Posts {
    int post1, post2, post3;

    public Posts(int post1, int post2, int post3) {
        this.post1 = post1;
        this.post2 = post2;
        this.post3 = post3;
    }

    public int getPost1() {
        return post1;
    }

    public int getPost2() {
        return post2;
    }

    public int getPost3() {
        return post3;
    }

    public void setPost1(int post1) {
        this.post1 = post1;
    }

    public void setPost2(int post2) {
        this.post2 = post2;
    }

    public void setPost3(int post3) {
        this.post3 = post3;
    }
}
